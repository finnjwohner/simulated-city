const GetEstimate = async (startLatLng : string, endLatLng : string) => {
    const uri : string = `https://maps.googleapis.com/maps/api/distancematrix/json?destinations=${encodeURIComponent(endLatLng)}&origins=${encodeURIComponent(startLatLng)}&key=xxx`;

    try {
        const response : any = await API<any>(uri);
        return response;
    }
    catch(error : any) {
        console.error(error);
        return null;
    }
}

const originDestinations : OriginDestination[] = [];


const GetRandomOriginDestination = async (index : number) => {
    const keys : string[] = Object.keys(nodes);

    const startNodeIdentifier = keys[keys.length * Math.random() << 0];
    const startNode : string = nodes[startNodeIdentifier];

    let endNodeIdentifier = null;

    do {
        endNodeIdentifier = keys[keys.length * Math.random() << 0];
    } while(endNodeIdentifier == startNodeIdentifier);

    const endNode : string = nodes[endNodeIdentifier];

    const response = await GetEstimate(startNode, endNode);

    const oD : OriginDestination = new OriginDestination(startNodeIdentifier, endNodeIdentifier, response.rows[0].elements[0].duration.value);
    originDestinations.push(oD);
    console.log(`[${index}] ${startNodeIdentifier} > ${endNodeIdentifier}: ${oD.estimatedTime} seconds...`);
}

